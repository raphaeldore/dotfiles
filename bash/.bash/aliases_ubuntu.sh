alias apti='sudo apt install'       # Installs a package
alias aptr='sudo apt remove'        # Removes a package
alias aptp='sudo apt purge'         # Removes package with configuration
alias aptu='sudo apt update'        # Refreshes repository index
alias aptfg='sudo apt full-upgrade' # Upgrades packages with auto-handling of dependencies
alias aptg='sudo apt upgrade'       # Upgrades all upgradable packages
alias apts='sudo apt search'        # Searches for the program
